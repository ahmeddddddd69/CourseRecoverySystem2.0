    private void loadFailedComponents() {
        DefaultTableModel model = (DefaultTableModel) tblFailedComponents.getModel();
        model.setRowCount(0);

        if (selectedStudent == null) {
            return;
        }

        ArrayList<Grade> grades = GradeDataHelper.loadGrades("data/student_course_grades.csv");

        for (Grade g : grades) {
            if (g.getStudentId().equals(selectedStudent.getStudentId())
                    && g.getGradePoint() < 2.0) {

                model.addRow(new Object[]{
                    g.getCourseCode(),      // Course
                    g.getComponentName(),   // Component
                    g.getGradePoint()       // Current Grade
                });
            }
        }
    }

    
    private void loadRecoveryTasks() {
        DefaultTableModel model = (DefaultTableModel) tblRecoveryTasks.getModel();
        model.setRowCount(0);

        if (selectedStudent == null) {
            return;
        }

        java.util.List<RecoveryPlan> plans =
                recoveryController.getRecoveryPlansByStudent(selectedStudent.getStudentId());

        for (RecoveryPlan plan : plans) {
            for (RecoveryTask t : plan.getTasks()) {
                model.addRow(new Object[]{
                    plan.getCourseCode(),   // Course
                    t.getWeek(),            // Week
                    t.getDescription(),     // Task Description
                    t.getStatus()           // Status
                });
            }
        }
    }

    
