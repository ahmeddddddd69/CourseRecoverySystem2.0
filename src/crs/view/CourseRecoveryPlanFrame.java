    private void btnLoadFailedComponentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadFailedComponentsActionPerformed
       
        String studentId = txtStudentId.getText().trim();
        
        if (studentId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Student ID.");
            return;
        }
        
        ArrayList<Student> students =
                StudentDataHelper.loadStudents("student_information.csv");
        
        Student found = null;
        for (Student s : students) {
            if (s.getStudentId().equals(studentId)) {
                found = s;
                break;
            }
        }
        
        if (found == null) {
            JOptionPane.showMessageDialog(this, "Student not found.");
            selectedStudent = null;
            ((DefaultTableModel) tblFailedComponents.getModel()).setRowCount(0);
            ((DefaultTableModel) tblRecoveryTasks.getModel()).setRowCount(0);
            return;
        }
        
        selectedStudent = found;
        loadFailedComponents();
        loadRecoveryTasks();
    
    

    }//GEN-LAST:event_btnLoadFailedComponentsActionPerformed

    private void btnAddTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTaskActionPerformed

        if (selectedStudent == null) {
            JOptionPane.showMessageDialog(this, "Load a student first (Load Failed Components).");
            return;
        }
        
        String course = txtCourse.getText().trim();
        String weekText = txtWeek.getText().trim();
        String desc = txtTaskDescription.getText().trim();
        
        if (course.isEmpty() || weekText.isEmpty() || desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in Course, Week and Task.");
            return;
        }
        
        int week;
        try {
            week = Integer.parseInt(weekText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Week must be a number.");
            return;
        }
        
        recoveryController.createRecoveryPlan(selectedStudent.getStudentId(), course);
        
        boolean ok = recoveryController.addTask(
                selectedStudent.getStudentId(),
                course,
                week,
                desc
        );
        
        if (ok) {
            JOptionPane.showMessageDialog(this, "Task added.");
            loadRecoveryTasks();
        
        } else {
            JOptionPane.showMessageDialog(this, "Could not add task.");
        }
    }//GEN-LAST:event_btnAddTaskActionPerformed

    private void btnDeleteTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteTaskActionPerformed

        if (selectedStudent == null) {
            JOptionPane.showMessageDialog(this, "Load a student first.");
            return;
        }
        
        int row = tblRecoveryTasks.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a task to delete.");
            return;
        }
        
        String course = tblRecoveryTasks.getValueAt(row, 0).toString();
        int week = Integer.parseInt(tblRecoveryTasks.getValueAt(row, 1).toString());
        
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Delete task for course " + course + ", week " + week + "?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION
        );
        
        if (confirm != JOptionPane.YES_OPTION) return;
        
        boolean ok = recoveryController.removeTask(selectedStudent.getStudentId(), course, week);
        
        if (ok) {
            JOptionPane.showMessageDialog(this, "Task deleted.");
            loadRecoveryTasks();
        } else {
            JOptionPane.showMessageDialog(this, "Could not delete task.");
        }
    }//GEN-LAST:event_btnDeleteTaskActionPerformed

    private void btnUpdateTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateTaskActionPerformed
        
        if (selectedStudent == null) {
            JOptionPane.showMessageDialog(this, "Load a student first.");
            return;
        }
        int row = tblRecoveryTasks.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a task to update.");
            return;
        }
        
        String course = tblRecoveryTasks.getValueAt(row, 0).toString();
        int oldWeek = Integer.parseInt(tblRecoveryTasks.getValueAt(row, 1).toString());
        
        String weekText = txtWeek.getText().trim();
        String desc = txtTaskDescription.getText().trim();
        
        if (weekText.isEmpty() || desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter new Week and Task description.");
            return;
        }
        
        int newWeek;
        try {
            newWeek = Integer.parseInt(weekText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Week must be a number.");
            return;
        }
        
        
        recoveryController.removeTask(selectedStudent.getStudentId(), course, oldWeek);
        recoveryController.addTask(selectedStudent.getStudentId(), course, newWeek, desc);
        
        JOptionPane.showMessageDialog(this, "Task updated.");
        loadRecoveryTasks();
    }//GEN-LAST:event_btnUpdateTaskActionPerformed

