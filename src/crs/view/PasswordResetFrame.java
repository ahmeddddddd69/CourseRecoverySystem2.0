    private void btnConfirmResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmResetActionPerformed
    
        System.out.println("RESET writing to: " + FileHelper.fixPath("users.txt"));
        System.out.println("âœ… Confirm button clicked"); 
        String username = txtUsername.getText().trim();
        String newPass  = new String(txtNewPassword.getPassword()).trim();
        
        if (username.isEmpty() || newPass.isEmpty()) {
            
            JOptionPane.showMessageDialog(
                    this,
                    "Please fill in both username and new password.",
                    "Missing Information",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        String fullPath = "src/data/users.txt"; 
        System.out.println("RESET writing to actual file: " + fullPath);
        ArrayList<String> lines = new ArrayList<>();
        boolean found = false;
        
        try (BufferedReader br = new BufferedReader(new FileReader(fullPath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 2 && parts[0].trim().equals(username)) {
                    parts[1] = newPass;
                    found = true;
                    
                    StringBuilder sb = new StringBuilder();
                    for (int i = 0; i < parts.length; i++) {
                        if (i > 0) sb.append(",");
                        sb.append(parts[i].trim());
                    }
                    line = sb.toString();
                
                }
                lines.add(line);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error reading users.txt:\n" + ex.getMessage(),
                    "File Error",
                    JOptionPane.ERROR_MESSAGE
            
            );
            return;
        
        }
        if (!found) {
            JOptionPane.showMessageDialog(
                    this,
                    "Username not found.",
                    "Reset Failed",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(fullPath, false))) {
            for (String l : lines) {
                bw.write(l);
                bw.newLine();
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error saving users.txt:\n" + ex.getMessage(),
                    "File Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        JOptionPane.showMessageDialog(
                this,
                "Password has been changed successfully.",
                "Success",
                JOptionPane.INFORMATION_MESSAGE
        );
        this.dispose();
    }//GEN-LAST:event_btnConfirmResetActionPerformed

